        function actualizarCantidad(id, operacion) {
            const cantidadElemento = document.getElementById(id);
            let cantidad = parseInt(cantidadElemento.innerText);
            if (operacion === 'incrementar') {
                cantidad++;
            } else if (operacion === 'decrementar' && cantidad > 0) {
                cantidad--;
            }
            cantidadElemento.innerText = cantidad;
        }

